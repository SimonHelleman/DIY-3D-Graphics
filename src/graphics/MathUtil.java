package graphics;

public class MathUtil {
    public static float sqrtf(float val) {
        return (float) Math.sqrt(val);
    }

    public static float sinf(float val) {
        return (float) Math.sin(val);
    }

    public static float cosf(float val) {
        return (float) Math.cos(val);
    }

    public static float tanf(float val) {
        return (float) Math.tan(val);
    }

    public static float asinf(float val) {
        return (float) Math.asin(val);
    }

    public static float acosf(float val) {
        return (float) Math.acos(val);
    }

    public static float atanf(float val) {
        return (float) Math.tan(val);
    }

    public static float atan2f(float y, float x) {
        return (float) Math.atan2(y, x);
    }

    public static float radiansf(float deg) {
        return (float) Math.toRadians(deg);
    }
}
